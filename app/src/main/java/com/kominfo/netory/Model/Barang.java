package com.kominfo.netory.Model;

public class Barang {
private String id, tahun,no_kontrak, nama_barang,kategori,serial_number,status_barang,lokasi_barang;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public String getNo_kontrak() {
        return no_kontrak;
    }

    public void setNo_kontrak(String no_kontrak) {
        this.no_kontrak = no_kontrak;
    }

    public String getNama_barang() {
        return nama_barang;
    }

    public void setNama_barang(String nama_barang) {
        this.nama_barang = nama_barang;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getSerial_number() {
        return serial_number;
    }

    public void setSerial_number(String serial_number) {
        this.serial_number = serial_number;
    }

    public String getStatus_barang() {
        return status_barang;
    }

    public void setStatus_barang(String status_barang) {
        this.status_barang = status_barang;
    }

    public String getLokasi_barang() {
        return lokasi_barang;
    }

    public void setLokasi_barang(String lokasi_barang) {
        this.lokasi_barang = lokasi_barang;
    }
}
